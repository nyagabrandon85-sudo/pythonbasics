#foor loop-for iterating through a sequence
#looping through list
fruits=["apple","banana","mangoes","oranges"]
for fruit in fruits:
    print(fruit)
#looping through a string
for x in "hello":
    print(x)
#looping through a range
for z in range(2,6):
    print(z)
for b in range(9):
    print(b)
#step
for c in range(20,30,2):
    print(c)