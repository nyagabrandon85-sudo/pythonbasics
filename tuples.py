#tuples are ordered, unchangeable and allow duplicates
courses=("HTML","CSS","PYTHON","BOOTSTRAP")
print(courses)
print(type(courses))
#accessing the item
print(courses[1])
#len()
print(len(courses))
#loop
for course in courses:
    print(course)
