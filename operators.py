#Arithmetic operators+ / * %
#Addition
x=20
y=30
print(f"The sum is {x+y}")
#Subtraction
print("difference between",y,"and",x,"is",y-x )
#Multiplication
print(f"the product of {x} and {y} is {x*y} ")
#division
print(f"the division of {x} and {y} is {x/y}")
#Modulas=% the remainder of division
print(f"the modulus of {x} and {y} is {y%x}")
#Power **
print(f"{x} power 2 is {x**2}")
#comparison operators > < >= <= ==
#greater than
print(f"is {x} greater than {y}? {x>y}")
#less than
print(f"is {x} less than {y}? {x<y}")
#less than or equal to<=
print(f"is {x} less than or equal to {y}? {x<=y}")
#greater than or equal to
print(f"is {x} greater than or equal to{y}? {x>y}")
#not equal to
print(f"is {x} not equal to {y}? {x!=y}")
#logical operators and, or and not
#and-returns if both statements are true
z=10
print(z<20 and z>5)
print(z>20 and z>5)
#or-returns true if one  statement is true
print(z>20 or z>5)
#not-returns the reverse
print(not(z>5))
#assignment operators =, +=, -=, *=
b=40
print(b)
#+=
b+=5 #b=b+5
print(b)
#-+
b-=3 #b=b-3
print(b)